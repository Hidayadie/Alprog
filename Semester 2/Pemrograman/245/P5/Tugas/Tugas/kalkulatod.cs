using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Tugas
{
    public class kalkulatod
    {
        public int penambahan(int a, int b)
        {
            return a + b;
        }
        public int penambahan(int a, int b, int c)
        {
            return a + b + c;
        }

        public int pengurangan(int a, int b)
        {
            return a - b;
        }

        public static int perkalian(int a, int b)
        {
            return a * b;
        }

        public float pembagian(float a, float b)
        {
            return a / b;
        }
    }
}
